namespace std {
    template<> struct hash<Vertex> {
        size_t operator()(Vertex const& vertex) const {
            return ((hash<cVec3>()(vertex.pos) ^
                (hash<cVec3>()(vertex.normal) << 1)) >> 1) ^
                (hash<cVec2>()(vertex.texCoord) << 1);
        }
    };
}

